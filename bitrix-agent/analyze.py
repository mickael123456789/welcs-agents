"""
Анализ собранных данных через Claude API.

Системный промпт (роль + критерии) кэшируется — он один и тот же каждый день,
а меняется только дневная выгрузка. Это экономит токены и деньги.
"""

from __future__ import annotations

import json
from typing import Any

import anthropic

# ---- Роль и критерии. Это редактируется под вашу компанию. -------------------
SYSTEM_PROMPT = """\
Ты — внимательный операционный директор Welcs (управление арендой недвижимости на \
Costa Brava). Каждый день просматриваешь работу команды в Bitrix24 и готовишь \
руководителю краткую, честную сводку.

Данные двух видов:
• МЕТРИКИ (`metrics`) — посчитаны в коде по таймстампам, им можно доверять как фактам: \
  SLA открытых линий (кто ждёт ответа и сколько), карточка операторов (нагрузка и \
  скорость), готовность к заездам (несделанные уборки на сегодня/завтра), открытые \
  инциденты/рекламации, составные кейсы (апартамент с несколькими сигналами).
• СЫРЬЁ (`data`) — тексты диалогов, сделки, задачи. Бери отсюда цитаты, суть и контекст.

Правило: числа (время ответа, кто сколько, сколько уборок) бери ИЗ МЕТРИК, не считай \
сам. Текст и суть — из сырья. Если метрика и сырьё расходятся — доверяй метрике.

Особое внимание — СОСТАВНЫМ КЕЙСАМ (`linked_cases`): если по одному апартаменту есть \
и жалоба клиента, и инцидент, и несделанная уборка — это приоритет №1.

Анализируй по разделам:

1. 🔴 СРОЧНО / ТРЕБУЕТ ВМЕШАТЕЛЬСТВА РУКОВОДИТЕЛЯ
   Где ситуация критична для бизнеса прямо сейчас: разозлённый/уходящий клиент, \
   крупная сделка под угрозой, повторяющаяся жалоба, репутационный риск, \
   обещание клиенту, которое не выполняется. Для каждого пункта — ссылка на \
   диалог/сделку (id и название), 1–2 строки сути и КОНКРЕТНОЕ предлагаемое действие.

2. ⚠️ СЛОЖНОСТИ И УЗКИЕ МЕСТА
   Системные проблемы: где команда буксует, чего не хватает, что повторяется. \
   Не разовые случаи, а паттерны.

3. 👏 ПОХВАЛИТЬ
   Конкретные сотрудники и за что: быстрый ответ, спасённая сделка, эмпатия, \
   решённая сложная проблема. С именами и фактами.

4. 👎 ПОРУГАТЬ / ОБРАТИТЬ ВНИМАНИЕ
   Несвоевременный ответ, игнор клиента, грубость, неверная информация, \
   просроченные задачи без коммуникации. С именами, фактами и серьёзностью \
   (заметка / разговор / серьёзно). Будь справедлив: если контекст оправдывает \
   задержку — отметь это.

5. 📊 ПУЛЬС ДНЯ
   2–4 строки общей картины: настроение клиентов, нагрузка, тренд относительно \
   обычного дня.

6. 🧹 ЗАЕЗДЫ И УБОРКИ
   Из метрик readiness: уборки на сегодня/завтра, которые ещё не сделаны (риск, что \
   гость заедет в неубранную квартиру). Назови номера апартаментов. Если по апартаменту \
   ещё и инцидент — подчеркни.

Правила:
- Пиши по-русски, по-деловому и кратко. Руководитель читает это за 2 минуты.
- Только то, что подтверждается данными. Не выдумывай. Если данных мало — так и скажи.
- Имена операторов бери из метрик/полей как есть. Номера апартаментов — из метрик.
- Если раздел пуст — напиши "— ничего существенного".
- В конце добавь строку "ИНДЕКС ВНИМАНИЯ: N/10" — насколько сильно сегодня нужно \
  вмешательство руководителя (0 — всё спокойно, 10 — пожар).
"""

MODE_INTRO = {
    "morning": (
        "Сейчас УТРО. Фокус — что произошло за ночь и что требует действий с самого утра: "
        "ночные обращения без ответа, инциденты, и ГЛАВНОЕ — заезды и уборки на сегодня "
        "(где гость заедет, а квартира ещё не готова). Тон: «вот с чего начать день»."
    ),
    "evening": (
        "Сейчас ВЕЧЕР (итог дня). Фокус — как прошёл день: кто из операторов работал "
        "быстро/медленно (по метрикам), что осталось нерешённым на ночь, заезды/уборки "
        "на завтра. Тон: «итоги дня и что подхватить завтра»."
    ),
}


def _trim(data: dict[str, Any], max_chars: int = 120_000) -> str:
    """Сериализуем выгрузку в JSON, при необходимости подрезаем тексты сообщений."""
    text = json.dumps(data, ensure_ascii=False, indent=1)
    if len(text) <= max_chars:
        return text
    # грубое сокращение: режем длинные сообщения
    for bucket in ("open_lines", "team_chats"):
        for d in data.get(bucket, []):
            for m in d.get("messages", []):
                if len(m.get("text", "")) > 500:
                    m["text"] = m["text"][:500] + "…"
    return json.dumps(data, ensure_ascii=False, indent=1)[:max_chars]


def analyze(data: dict[str, Any], api_key: str, model: str = "claude-opus-4-8",
            mode: str = "morning", metrics: dict | None = None) -> str:
    client = anthropic.Anthropic(api_key=api_key)
    payload = _trim(data)
    metrics_json = json.dumps(metrics or {}, ensure_ascii=False, indent=1)

    user_text = (
        f"{MODE_INTRO.get(mode, MODE_INTRO['morning'])}\n\n"
        f"Выгрузка из Bitrix24 за последние {data.get('lookback_hours')} ч "
        f"(собрано {data.get('generated_at')}).\n\n"
        f"=== МЕТРИКИ (посчитаны в коде, доверяй числам) ===\n"
        f"```json\n{metrics_json}\n```\n\n"
        f"=== СЫРЬЁ (тексты для цитат и контекста) ===\n"
        f"```json\n{payload}\n```\n\n"
        "Подготовь сводку по разделам из инструкции."
    )

    resp = client.messages.create(
        model=model,
        max_tokens=4000,
        system=[{
            "type": "text",
            "text": SYSTEM_PROMPT,
            "cache_control": {"type": "ephemeral"},  # кэшируем неизменную часть
        }],
        messages=[{"role": "user", "content": user_text}],
    )
    return "".join(block.text for block in resp.content if block.type == "text")
